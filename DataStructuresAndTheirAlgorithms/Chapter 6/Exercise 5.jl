Q.

Which is more suitable if the list is implemented as a table in contiguous memory, the Move-to-Front Heuristic or the Transpose Heuristic?
Why?

A.

Since it is implemented in contiguous memory, to move an element to the first, we should move almost (n+1)/2 elements, while swapping only needs three elements assignments.
By this reason, the MTF Heuristic takes n times more time, which makes Transpose Heuristic be more suitable.

